"""
na função print, a virgula (fora de aspas) é usada para separador os valores
"""
print(17, 2)

"""
podemos também usar sep='' pra trocar a virgula pelo valor desejado
"""
print(17, 2, sep="-")

"""
podemos também usar end='' determinar o que acontece ao final da linha
"""
print(17, 2, sep="-", end='##')
print(17, 2, sep="_")